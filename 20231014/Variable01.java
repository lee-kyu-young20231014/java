public class Variable01 {

	public static void main(String[] args) {
//		 주석은 해석되지 않는 문장..
//		 활용 : 해당코드의 내용을 설명할때
//		정수를 저장할수 있는 변수명 days를 만들고 10을 할당
		int days = 10; 
//		변수의 값을 출력한다.
		System.out.println(days);
		
//		변수를 선언(정의) 하고 그 이후에 값을 할당
		int speed;
		speed = 20;
//		변수의 값을 출력한다
		System.out.println(speed);		
	}
}
